#include <windows.h>
#include <stdio.h>
#include "ErrorFormatter.h"
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
namespace NHelpers
{
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const char *GetLastError()
{
	static char szMessage[256];
	FormatMessage( FORMAT_MESSAGE_FROM_SYSTEM, 0, ::GetLastError(), 0, szMessage, 256, 0 );

	static char szResult[256];
	sprintf_s( szResult, "%s (%d)", szMessage, ::GetLastError() );
	return szResult;
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}