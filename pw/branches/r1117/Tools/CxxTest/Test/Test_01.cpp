#include "../CxxTest/CxxTest.h"
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class CMyFirstTestSuite : public CxxTest::TestSuite
{
public:
	void Test()
	{
		int i = 1;
		TS_ASSERT( i == 1 );
	}
};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CXX_REGISTER_TEST_SUITE( CMyFirstTestSuite )
{
	CXX_REGISTER_TEST_FUNCTION( Test );
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CXX_TEST( Test01 )
{
	int i = 1;
	TS_ASSERT( i == 2 );
	TS_ASSERT( i == 3 );
	i = 0;
	int n = 10/i;
	TS_ASSERT( n == 0);
}