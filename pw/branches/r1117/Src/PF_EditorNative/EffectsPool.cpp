#include "stdafx.h"
#include "EffectsPool.h"
#include "../PF_Core/EffectsPool.h"
#include "../PF_Core/SpectatorEffectsPool.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
namespace PF_EditorNative
{
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void EffectsPool::Initialize()
{
  NDb::Ptr<NDb::EffectsPool> pEffects = NDb::Get<NDb::EffectsPool>( NDb::DBID( "Effects/_.EFPL" ) );
  PF_Core::EffectsPool::Init( pEffects );
  bool isSpectator = false;
  PF_Core::SpectatorEffectsPool::Init<bool>( isSpectator );
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void EffectsPool::Release()
{
  PF_Core::EffectsPool *pool = PF_Core::EffectsPool::Get();
  if ( pool )
    pool->TearDown();
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void EffectsPool::Reset()
{
  PF_Core::KillAllActiveEffects();
}
} // namespace PF_EditorNative
