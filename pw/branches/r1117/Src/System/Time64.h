#ifndef __TIME64_H__
#define __TIME64_H__
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma once
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline UINT64 GetLongTickCount()
{
	FILETIME sysTime;
	GetSystemTimeAsFileTime( &sysTime );
	return ( ( UINT64( sysTime.dwHighDateTime ) << 32 ) | sysTime.dwLowDateTime ) / 10000;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#endif // __TIME64_H__
