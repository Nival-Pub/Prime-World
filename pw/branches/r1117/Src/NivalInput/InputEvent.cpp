#include "stdafx.h"
#include "InputEvent.h"

BASIC_REGISTER_CLASS( Input::ICommandInstance );

namespace Input
{

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Event::Event() :
type( EEventType::Null ), scalar( 0 )
{
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Event::Event( ICommandInstance * cmd ) :
command( cmd ), type( EEventType::Activation ), scalar( 0 )
{
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Event::Event( ICommandInstance * cmd, float _scalar, bool deltaMode ) :
command( cmd ), type( deltaMode ? EEventType::ScalarDelta : EEventType::ScalarValue ), scalar( _scalar )
{
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Event::Event( ICommandInstance * cmd, const SSysParams & sys ) :
command( cmd ), type( EEventType::System ), scalar( 0 ), sysParams( sys )
{
}

} //namespace Input
