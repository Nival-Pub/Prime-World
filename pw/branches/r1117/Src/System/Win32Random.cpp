#include "stdafx.h"

#include "Win32Random.h"
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
namespace NWin32Random
{
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
static int s_holdrand = 0;
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Seed( const int nSeed )
{
	s_holdrand = nSeed;
}
int GetSeed()
{
	return s_holdrand;
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
unsigned int Random() 
{ 
	return ( ((s_holdrand = s_holdrand * 214013L + 2531011L) >> 16) & 0x7fff );
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
};
